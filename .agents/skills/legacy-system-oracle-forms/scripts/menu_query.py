"""
Menu Query Tool - Interactive Menu Inventory Explorer
======================================================

Purpose:
    Queries the generated menu_inventory.json to find specific menu items,
    filter by role, or simulate the exact menu tree a user would see.

Input:
    - legacy-system/reference-data/inventories/menu_inventory.json
      (Generated by menu_builder.py)

Output:
    - Console output or JSON showing matching menu items

Key Features:
    - Query by application (--app AppFour)
    - Query by role (--role APP2_CLERK)
    - Query by item ID (--item FORM0000)
    - Simulate full menu tree (--simulate)

Example Output:
    {
      "item": "FORM0000",
      "label": "Criminal Inquiry",
      "visible_to": ["APP2_COURTS_CLERK", "APP2_SYSADMIN"],
      "enabled_for": ["APP2_SYSADMIN"]
    }

Usage:
    python menu_query.py --app AppFour --role APP2_CLERK
    python menu_query.py --item FORM0000
    python menu_query.py --app AppFour --simulate --role APP2_SYSADMIN

Related:
    - menu_builder.py: Generates the inventory this tool queries
    - ADR-0002: Menu Configuration as Code
"""

